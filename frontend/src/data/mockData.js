const makeSeats = (total, pricing) => {
  const rows = ['A', 'B', 'C', 'D', 'E', 'F', 'G', 'H'];
  const seats = [];
  let count = 0;
  let ri = 0;
  while (count < total && ri < rows.length) {
    const perRow = Math.min(10, total - count);
    for (let n = 1; n <= perRow; n++) {
      let type = 'regular';
      let price = pricing.regular;
      if (ri < 2) { type = 'premium'; price = pricing.premium || pricing.regular * 1.5; }
      else if (ri < 4) { type = 'vip'; price = pricing.vip || pricing.regular * 1.2; }
      seats.push({ row: rows[ri], number: n, type, price: Math.round(price), isBooked: false });
      count++;
    }
    ri++;
  }
  return seats;
};

